package com.liga.entidades.extras;

import java.util.Date;

import com.liga.entidades.EquipoTemporada;
public class TemporadaA {
	private EquipoTemporada campeon;
	private Date ff;
	private int numeroe;
	public TemporadaA() {
	}

	public EquipoTemporada getCampeon() {
		return campeon;
	}
	public void setCampeon(EquipoTemporada campeon) {
		this.campeon = campeon;
	}
	public Date getFf() {
		return ff;
	}
	public void setFf(Date ff) {
		this.ff = ff;
	}
	public int getNumeroe() {
		return numeroe;
	}
	public void setNumeroe(int numeroe) {
		this.numeroe = numeroe;
	}
}
